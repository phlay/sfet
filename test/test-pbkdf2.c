const size_t tablenum = sizeof(table) / sizeof(table[0]);		
	


int main()
{
	uint8_t check[64];
	int i;

	/* test pbkdf2 */
	for (i = 0; i < tablenum; i++) {
		pbkdf2_hmac_sha256(check, 64,
				   table[i].passwd, strlen(table[i].passwd),
				   (uint8_t*)table[i].salt, strlen(table[i].salt),
				   table[i].c);

		if (memcmp(check, table[i].dk, 64) != 0) {
			fprintf(stderr, "test nr. %d failed\n", i+1);
			return 1;
		}
	}

	return 0;
}
